    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        // TODO add your handling code here:
        try{
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);

            String sql = "SELECT * FROM dbadmin WHERE username='"+txtusername.getText()+"' and password='"+txtpassword.getText()+"' and level ='"+CBlevel.getSelectedItem()+"'";
            ResultSet r = s.executeQuery(sql);
            r.last();
            if(txtusername.getText().equals("username") && txtpassword.getText().equals("password") && CBlevel.getSelectedItem().equals("level")){
                a.setVisible(true);
                data_costumer.setEnabled(true);
                database_admin.setEnabled(true);
                salary_admin.setEnabled(true);
                database_admin.setEnabled(true);
                txtusername.setText("");
                txtpassword.setText("");
                CBlevel.setSelectedItem("pemilik");
                this.dispose();
            
            }else if(r.getRow()>=1){
                if(CBlevel.getSelectedItem().equals("pemilik")){
                    a.setVisible(true);
                    data_costumer.setEnabled(true);
                database_admin.setEnabled(true);
                salary_admin.setEnabled(true);
                database_admin.setEnabled(true);
                txtusername.setText("");
                txtpassword.setText("");
                CBlevel.setSelectedItem("pemilik");
                this.dispose();
                
                }else if (CBlevel.getSelectedItem().equals("admin")){
                    a.setVisible(true);
                    data_costumer.setEnabled(true);
                    database_admin.setEnabled(true);
                database_admin.setEnabled(false);
                salary_admin.setEnabled(false);
                txtusername.setText("");
                txtpassword.setText("");
                CBlevel.setSelectedItem("admin");
                this.dispose();
            } 
            }else if((r.getRow()==0)){
                JOptionPane.showMessageDialog(null, "USERNAME ATAU PASSWORD SALAH");
                data_costumer.setEnabled(false);
                database_admin.setEnabled(false);
                salary_admin.setEnabled(false);
                database_admin.setEnabled(false);
            }
            s.close();
        
        }catch(SQLException e){
            System.out.println("LOGIN ERROR"+e);
        }
    }//GEN-LAST:event_btnloginActionPerformed

